package io.jc.studentapp.student.model;

public class Quote {
	
	String blockQuote;
	String footer;
	String cite;
	
	/**
	 * @return the blockQuote
	 */
	public String getBlockQuote() {
		return blockQuote;
	}
	/**
	 * @param blockQuote the blockQuote to set
	 */
	public void setBlockQuote(String blockQuote) {
		this.blockQuote = blockQuote;
	}
	/**
	 * @return the footer
	 */
	public String getFooter() {
		return footer;
	}
	/**
	 * @param footer the footer to set
	 */
	public void setFooter(String footer) {
		this.footer = footer;
	}
	/**
	 * @return the cite
	 */
	public String getCite() {
		return cite;
	}
	/**
	 * @param cite the cite to set
	 */
	public void setCite(String cite) {
		this.cite = cite;
	}


}
